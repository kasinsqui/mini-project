    private Login pegarDadosDaTela() {
        String usuario = txtUsuario.getText();
        String senha = String.valueOf(txtSenha.getPassword());

        Login login = new Login();
        login.setNome(usuario);
        login.setSenha(senha);

        return login;
    }

