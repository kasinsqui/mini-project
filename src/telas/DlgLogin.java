    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        Login login = pegarDadosDaTela();
        login = dao.consultarPorLogin(login);
        boolean acesso = dao.realizarLogin(login);

        if (acesso) {
            UsuarioDAO usuarioDAO = new UsuarioDAO();
            Usuario usuario = usuarioDAO.consultaPorLogin(login);
            FrmMenu menu = new FrmMenu(usuario);
            menu.setVisible(true);
            menu.setFocusable(true);
            this.dispose();
        } else {
            txtSenha.setText("");
            txtSenha.requestFocus();
            JOptionPane.showMessageDialog(null, "Usuário ou senha inválido(s)!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

